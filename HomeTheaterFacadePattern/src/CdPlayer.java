
public class CdPlayer {
	public void on() {
		System.out.println("Cd player is on");
	}
	
	public void off() {
		System.out.println("Cd player is off");
	}
	
	public void eject() {
		System.out.println("Cd player eject");
	}
	
	public void pause() {
		System.out.println("Cd player paused");
	}
	
	public void play() {
		System.out.println("Cd player play");
	}
	
	public void stop() {
		System.out.println("Cd player stopped");
	}

}
