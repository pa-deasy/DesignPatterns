
public class DvdPlayer {
	public void on() {
		System.out.println("Dvd player on");
	}
	
	public void off() {
		System.out.println("Dvd player off");
	}
	
	public void eject() {
		System.out.println("Dvd player eject");
	}
	
	public void pause() {
		System.out.println("Dvd player paused");
	}
	
	public void play() {
		System.out.println("Dvd player play");
	}
	
	public void setSurroundAudio() {
		System.out.println("Dvd player surround audio set");
	}
	
	public void setTwoChannelAudio() {
		System.out.println("Dvd player 2 channel audio set");
	}
	public void stop() {
		System.out.println("Dvd player stopped");
	}

}
